
--Comando SQL item a)
SELECT C.CD_CATEGORIA, C.DS_CATEGORIA,
        P.CD_PRODUTO, P.CD_PRODUTO,P.DS_PRODUTO,
        P.VL_UNITARIO, P.TP_EMBALAGEM, P.VL_PERC_LUCRO
    FROM MC_CATEGORIA_PROD C LEFT JOIN MC_PRODUTO P
    ON(C.CD_CATEGORIA = P.CD_PRODUTO)
ORDER BY C.DS_CATEGORIA ASC, P.DS_PRODUTO ASC;
        

--Comando SQL item b)        
SELECT C.NR_CLIENTE, C.NM_CLIENTE,
        C.DS_EMAIL, C.NR_TELEFONE,
        C.NM_LOGIN,
        TO_CHAR(PF.DT_NASCIMENTO, 'DD-MM-YYYY') AS DATA_NASCIMENTO,
        TO_CHAR(PF.DT_NASCIMENTO, 'DAY', 'NLS_DATE_LANGUAGE = PORTUGUESE') AS DIA_SEMANA_NASC,
        FLOOR(MONTHS_BETWEEN(SYSDATE, PF.DT_NASCIMENTO) / 12) AS IDADE,
        PF.FL_SEXO_BIOLOGICO,
        PF.NR_CPF
    FROM  MC_CLIENTE C JOIN MC_CLI_FISICA PF 
    ON C.NR_CLIENTE = PF.NR_CLIENTE;


--Comando SQL item C)    
SELECT V.CD_PRODUTO, P.DS_PRODUTO,
       V.DT_VISUALIZACAO
   FROM MC_SGV_VISUALIZACAO_VIDEO V JOIN MC_PRODUTO P 
   ON V.CD_PRODUTO = P.CD_PRODUTO
ORDER BY  V.DT_VISUALIZACAO DESC;


